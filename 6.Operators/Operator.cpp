#include <iostream>
using namespace std;

int main()
{
    /*Operator > Binary Operator + Unary Operator
     *             (+,-,*,/,%)       (-- , ++)
     *                        (increment , decrement)
     *                 (Preincrement ++a),(PostIncrement a++)
     *
     */

    // preincrement
    int a = 10;
    cout << "a " << a << endl;
    int b = ++a;
    cout << "b " << b << endl;

    // postincrement
    int c = 20;
    cout <<"c "<< c << endl;
    int d = c++;
    cout <<"d "<< d << endl;

    // relational operator
    
}
